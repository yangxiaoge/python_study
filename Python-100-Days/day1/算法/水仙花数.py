# -*- coding: utf-8 -*-
"""
 找出 100~999 之间的所有水仙花数
水仙花数是各位立方和等于这个数本身的数
如: 153 = 1**3 + 5**3 + 3**3
"""

for x in range(100, 1000):
    low = x % 10  # 取余
    mid = x // 10 % 10  # 先取整数（地板除），再取余数
    high = x // 100 % 10  # 同上

    if x == low ** 3 + mid ** 3 + high ** 3:
        print('%d 是水仙花数' % x)

